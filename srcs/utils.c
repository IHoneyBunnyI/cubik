#include "cub3d.h"

int		valid_line(char *line)
{
	int i;
	int flag;

	i = 0;
	flag = 0;
	while (line[i])
	{
		if (line[i] != ' ' && line[i] != '0' && line[i] != '1' && line[i] != '2'
		&& line[i] != 'W' && line[i] != 'E' && line[i] != 'N' && line[i] != 'S')
			return (0);
		i++;
	}
	return (1);
}
